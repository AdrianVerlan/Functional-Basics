def inputINT(write):
   return int(input(write))

m= inputINT("Write down firs integer: ")
n= inputINT("Write down second integer: ")

print(m+n)

def inputFloat(write):
   return float(input(write))

p= inputFloat("Write down firs integer: ")
o= inputFloat("Write down second integer: ")

print(p+o)


